def nextSeq(inArr):
    f0, f1, f2 = inArr

    A = f2 - 2*f1 + f0
    B = f1 - f0 - A
    C = f0

    f3 = A * (3**2) + B * 3 + C
    f4 = A * (4**2) + B * 4 + C
    f5 = A * (5**2) + B * 5 + C

    return [f3, f4, f5]

# 示例
inArr = [1, 2, 3]
print(nextSeq(inArr))  # 输出: [4, 5, 6]
